    private void jToggleButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jToggleButton1ActionPerformed
        // TODO add your handling code here:       
        try {
            if (!jTextField1.getText().isBlank() && !jTextField1.getText().isEmpty()
                    && !jTextField3.getText().isEmpty()
                    && !jTextField1.getText().isBlank()
                    && !jTextArea1.getText().isEmpty()
                    && !jTextArea1.getText().isBlank()
                    && cbx_deportes.getSelectedItem() != null) {

                cls_funcionesM2 DPT = new cls_funcionesM2();
                String nombreRutina = jTextField1.getText();
                String deporteAsignado = (String) cbx_deportes.getSelectedItem();
                String descripcion = jTextArea1.getText();
                String duracion = jTextField3.getText();
                boolean Status = true;

                //Instancia 
                DPT.crearRutinas(nombreRutina, deporteAsignado, descripcion, duracion, Status);
                JOptionPane.showMessageDialog(null, "¡Rutina creada correctamente!");
                limpiar();
            } else {
                JOptionPane.showMessageDialog(null, "Debe rellenar los datos");
            }
        } catch (Exception e) {
            dispose();
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }

