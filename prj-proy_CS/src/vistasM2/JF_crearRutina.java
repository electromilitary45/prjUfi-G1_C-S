    private void llenarcbx() {
        String vec[] = new String[999];
        int cont = 0;
        new cls_funcionesM2().llenarcbxdeportes(vec);
        for (int i = 0; i < vec.length; i++) {
            if (vec[i] != null) {
                cont++;
            }
        }
        String vec2[] = new String[cont];
        cont = 0;
        for (int i = 0; i < vec.length; i++) {
            if (vec[i] != null) {
                vec2[cont] = vec[i];
                cont++;
            }
        }
        cbx_deportes.setModel(new DefaultComboBoxModel<>(vec2));
    }
